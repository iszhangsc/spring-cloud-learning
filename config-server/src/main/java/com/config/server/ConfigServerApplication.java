/**
 * 配置中心服务端启动类
 * @author zhangshichang
 * @date 2019/9/4 下午5:42
 */
@EnableConfigServer
@SpringBootApplication
public class ConfigServerApplication {

    public static void main(String[] args) {
        SpringApplication.run(ConfigServerApplication.class, args);
    }

}
